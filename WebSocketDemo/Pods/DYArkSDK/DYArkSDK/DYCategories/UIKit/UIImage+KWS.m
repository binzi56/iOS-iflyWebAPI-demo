@implementation UIImage (KWSResizable)

+ (UIImage *)resizableImageNamed:(NSString *)imageName
{
    return [[UIImage imageNamed:imageName] resizableImage];
}

- (UIImage *)resizableImage
{
    return [self resizableImageWithCapInsets:UIEdgeInsetsMake(self.size.height * 0.5,
                                                              self.size.width * 0.5,
                                                              self.size.height * 0.5,
                                                              self.size.width * 0.5)];
}

@end

@implementation UIImage (KWGIF)

+ (UIImage *)kw_animatedGIFNamed:(NSString *)name
{
    NSData *data = nil;
    
    CGFloat scale = [UIScreen mainScreen].scale;
    if (scale > 2.0f){
        NSString *retinaFilePath = [[NSBundle mainBundle] pathForResource:[name stringByAppendingString:@"@3x"] ofType:@"gif"];
        data = [NSData dataWithContentsOfFile:retinaFilePath];
    }
    if (data == nil && scale > 1.0f){
        NSString *retinaFilePath = [[NSBundle mainBundle] pathForResource:[name stringByAppendingString:@"@2x"] ofType:@"gif"];
        data = [NSData dataWithContentsOfFile:retinaFilePath];
    }
    if (data == nil){
        NSString *filePath = [[NSBundle mainBundle] pathForResource:name ofType:@"gif"];
        data = [NSData dataWithContentsOfFile:filePath];
    }
    
    if (data){
        return [self kw_animatedGIFWithData:data];
    }
    else{
        return [UIImage imageNamed:name];
    }
    
}

+ (UIImage *)kw_animatedGIFWithData:(NSData *)data
{
    return [UIImage yy_imageWithSmallGIFData:data scale:[UIScreen mainScreen].scale];
}

@end


